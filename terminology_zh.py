# 依詞長降序，替換時先匹配較長詞
TERMINOLOGY_S2TW_SORTED = sorted(
    TERMINOLOGY_S2TW,
    key=lambda x: (-len(x[0]), x[0]),
)


def apply_terminology(text: str) -> str:
    """對已轉繁體的文本套用兩岸用語替換。"""
    if not text or not text.strip():
        return text
    result = text
    for s, t in TERMINOLOGY_S2TW_SORTED:
        if s in result:
            result = result.replace(s, t)
    return result
